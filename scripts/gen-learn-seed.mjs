// ─────────────────────────────────────────────────────────────────────────────
// gen-learn-seed.mjs
// Builds supabase/migrations/015_learn_seed.sql from:
//   • the manifest below (5 categories + all 14 Trainual subjects), and
//   • the per-subject JSON in ../iat-learn/_import/ produced by the PDF importers.
//
// Also writes 6 chunk files for pasting into the Supabase SQL editor one-at-a-time
// (the combined file is too large for the editor paste buffer):
//   015a_setup.sql        — categories + all module rows (tiny, run first)
//   015b_onboarding.sql   — onboarding lessons
//   015c_company.sql      — company lessons
//   015d_safety.sql       — safety lessons
//   015e_technical.sql    — technical training lessons
//   015f_products.sql     — products & tools lessons
//
// Run:  node scripts/gen-learn-seed.mjs
// ─────────────────────────────────────────────────────────────────────────────
import { readFileSync, writeFileSync, readdirSync, existsSync } from 'node:fs'
import { dirname, resolve, join } from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const IMPORT_DIR = resolve(__dirname, '../..', 'iat-learn/_import')
const MIGRATIONS = resolve(__dirname, '..', 'supabase/migrations')

const CATEGORIES = [
  { slug: 'onboarding', name: 'Onboarding', icon: 'Rocket', accent: '#089447',
    description: "Your first days at IAT — who we are, how we work, and the tools you'll use." },
  { slug: 'company', name: 'Company', icon: 'Building2', accent: '#0f766e',
    description: 'Policies, commitments, and the bigger picture of Innovative Air Technologies.' },
  { slug: 'safety', name: 'Safety', icon: 'ShieldCheck', accent: '#b45309',
    description: "Stay safe on the floor and in the field with IAT's safety procedures." },
  { slug: 'technical-training', name: 'Technical Training', icon: 'Wrench', accent: '#1d4ed8',
    description: 'Hands-on technical know-how — engineering, wiring, testing, and shipping.' },
  { slug: 'products-tools', name: 'Products & Tools', icon: 'Package', accent: '#7c3aed',
    description: 'Our product lines and the software tools that support them.' },
]

// All 14 subjects in display order. `file` is the source PDF (provenance).
const MODULES = [
  { cat: 'onboarding', slug: 'welcome-to-iat', title: 'Welcome to IAT', file: 'Welcome to IAT.pdf' },
  { cat: 'onboarding', slug: 'new-rep-onboarding', title: 'New Rep Onboarding', file: 'New Rep Onboarding.pdf' },
  { cat: 'onboarding', slug: 'how-we-use-trainual-at-iat', title: 'How we Use Trainual at IAT', file: 'How we Use Trainual at IAT.pdf' },
  { cat: 'company', slug: 'company-commitments', title: 'Company Commitments', file: 'Company Commitments.pdf' },
  { cat: 'company', slug: 'company-policies', title: 'Company Policies', file: 'Company Policies.pdf' },
  { cat: 'company', slug: 'iat', title: 'IAT', file: 'IAT.pdf' },
  { cat: 'safety', slug: 'safety-procedures', title: 'Safety Procedures', file: 'Safety Procedures.pdf' },
  { cat: 'technical-training', slug: 'dessicant-dehumidification-training', title: 'Dessicant Dehumidification Training', file: 'Dessicant Dehumidification Training.pdf' },
  { cat: 'technical-training', slug: 'electrical-wiring-training', title: 'Electrical Wiring Training', file: 'Electrical Wiring Training.pdf' },
  { cat: 'technical-training', slug: 'engineering', title: 'Engineering', file: 'Engineering.pdf' },
  { cat: 'technical-training', slug: 'testing-training', title: 'Testing Training', file: 'Testing Training.pdf' },
  { cat: 'technical-training', slug: 'shipping-training', title: 'Shipping Training', file: 'Shipping Training.pdf' },
  { cat: 'products-tools', slug: 'our-products', title: 'Our Products', file: 'Our Products.pdf' },
  { cat: 'products-tools', slug: 'using-dryware', title: 'Using DryWare', file: 'Using DryWare.pdf' },
]

const q = (v) => (v === null || v === undefined ? 'NULL' : `'${String(v).replace(/'/g, "''")}'`)

const header = (label) => [
  `-- ${'─'.repeat(77)}`,
  `-- ${label}`,
  `-- Generated by scripts/gen-learn-seed.mjs — do not edit by hand.`,
  `-- Idempotent: ON CONFLICT DO NOTHING.`,
  `-- ${'─'.repeat(77)}`,
  '',
]

// Load importer JSON keyed by moduleSlug.
const imported = new Map()
if (existsSync(IMPORT_DIR)) {
  for (const f of readdirSync(IMPORT_DIR)) {
    if (!f.endsWith('.json')) continue
    try {
      const raw = readFileSync(join(IMPORT_DIR, f), 'utf8').replace(/^﻿/, '')
      const data = JSON.parse(raw)
      if (data?.moduleSlug && Array.isArray(data.lessons)) imported.set(data.moduleSlug, data)
    } catch (e) {
      console.warn(`! Skipped ${f}: ${e.message}`)
    }
  }
}

// ── Build per-module lesson lines, keyed by category slug ──────────────────
const lessonLinesByCat = {}
for (const cat of CATEGORIES) lessonLinesByCat[cat.slug] = []

let importedCount = 0, pendingCount = 0, lessonCount = 0
const moduleLines = []

MODULES.forEach((m, mi) => {
  const data = imported.get(m.slug)
  const isImported = !!data && data.lessons.length > 0
  const status = isImported ? 'imported' : 'pending'
  const description = isImported
    ? (data.moduleSummary || null)
    : `Staged from ${m.file}. Content import pending.`
  if (isImported) importedCount++; else pendingCount++

  moduleLines.push(`-- ── ${m.title} (${status}) ──`)
  moduleLines.push(
    `INSERT INTO learn_modules (category_id, title, slug, description, display_order, is_published, source_file, import_status)\n` +
    `SELECT c.id, ${q(m.title)}, ${q(m.slug)}, ${q(description)}, ${mi}, TRUE, ${q(m.file)}, ${q(status)}\n` +
    `FROM learn_categories c WHERE c.slug = ${q(m.cat)}\n` +
    `ON CONFLICT (category_id, slug) DO NOTHING;`,
  )

  const lessons = isImported
    ? data.lessons.map((l, li) => ({
        title: l.title, slug: l.slug || `lesson-${li + 1}`,
        content: l.html || '', minutes: l.estimatedMinutes || 3, published: true,
      }))
    : [{
        title: 'Content import pending',
        slug: 'content-import-pending',
        content: `<figure class="img-missing"><figcaption>📷 This subject (${m.file}) hasn't been imported yet — an admin can add lessons here, or it will be filled in on the next import pass.</figcaption></figure>`,
        minutes: 1, published: true,
      }]

  const catLines = lessonLinesByCat[m.cat]
  catLines.push(`-- ── Lessons: ${m.title} ──`)
  lessons.forEach((l, li) => {
    lessonCount++
    const stmt =
      `INSERT INTO learn_lessons (module_id, title, slug, content, display_order, is_published, estimated_minutes)\n` +
      `SELECT m.id, ${q(l.title)}, ${q(l.slug)}, ${q(l.content)}, ${li}, ${l.published ? 'TRUE' : 'FALSE'}, ${l.minutes}\n` +
      `FROM learn_modules m JOIN learn_categories c ON c.id = m.category_id\n` +
      `WHERE c.slug = ${q(m.cat)} AND m.slug = ${q(m.slug)}\n` +
      `ON CONFLICT (module_id, slug) DO NOTHING;`
    catLines.push(stmt)
  })
  catLines.push('')
})

// ── 015a: categories + all module rows ─────────────────────────────────────
const aLines = [
  ...header('015a_setup.sql — Step 1 of 6: categories and module rows'),
  '-- ── Categories ──',
]
CATEGORIES.forEach((c, i) => {
  aLines.push(
    `INSERT INTO learn_categories (name, slug, description, icon, accent, display_order)\n` +
    `VALUES (${q(c.name)}, ${q(c.slug)}, ${q(c.description)}, ${q(c.icon)}, ${q(c.accent)}, ${i})\n` +
    `ON CONFLICT (slug) DO NOTHING;`,
  )
})
aLines.push('')
aLines.push('-- ── Modules ──')
aLines.push(...moduleLines)

// ── chunk files: one per category ──────────────────────────────────────────
const CHUNKS = [
  { file: '015b_onboarding.sql',  cat: 'onboarding',        step: '2 of 6' },
  { file: '015c_company.sql',     cat: 'company',           step: '3 of 6' },
  { file: '015d_safety.sql',      cat: 'safety',            step: '4 of 6' },
  { file: '015e_technical.sql',   cat: 'technical-training', step: '5 of 6' },
  { file: '015f_products.sql',    cat: 'products-tools',    step: '6 of 6' },
]

// ── Write chunk files ───────────────────────────────────────────────────────
const write = (filename, lines) => {
  const path = join(MIGRATIONS, filename)
  writeFileSync(path, lines.join('\n'), 'utf8')
  const kb = Math.round(Buffer.byteLength(lines.join('\n'), 'utf8') / 1024)
  console.log(`  ✓ ${filename}  (${kb} KB)`)
}

write('015a_setup.sql', aLines)
for (const chunk of CHUNKS) {
  const catMeta = CATEGORIES.find(c => c.slug === chunk.cat)
  const lines = [
    ...header(`${chunk.file} — Step ${chunk.step}: ${catMeta.name} lessons`),
    ...lessonLinesByCat[chunk.cat],
  ]
  write(chunk.file, lines)
}

// ── Combined 015_learn_seed.sql (for CLI / psql use) ───────────────────────
const combined = [
  ...header('015_learn_seed.sql — COMBINED (use chunk files for SQL editor paste)'),
  ...aLines,
  '',
  ...CHUNKS.flatMap(chunk => lessonLinesByCat[chunk.cat]),
]
write('015_learn_seed.sql', combined)

console.log()
console.log(`  categories: ${CATEGORIES.length}`)
console.log(`  modules:    ${MODULES.length}  (${importedCount} imported, ${pendingCount} pending)`)
console.log(`  lessons:    ${lessonCount}`)
console.log(`  import JSON found: ${imported.size}`)
